// // // 4. **`includes(searchString, position)`**: Checks if the string contains the specified substring.

// // // 5. **`endsWith(searchString, length)`**: Determines if the string ends with the specified substring.

// // // 6. **`indexOf(searchValue, fromIndex)`**: Returns the index of the first occurrence of the specified substring, or `-1` if not found.

// // // 7. **`lastIndexOf(searchValue, fromIndex)`**: Returns the index of the last occurrence of the specified substring, or `-1` if not found.

// // // 8. **`localeCompare(compareString)`**: Compares two strings in the current locale.

// // // 9. **`match(regexp)`**: Retrieves the result of matching a string against a regular expression.

// // // 10. **`padEnd(targetLength, padString)`**: Pads the string with another string (repeated, if necessary) until it reaches the specified length.

// // // 11. **`padStart(targetLength, padString)`**: Pads the string with another string (repeated, if necessary) until it reaches the specified length, starting from the beginning.

// // // 12. **`repeat(count)`**: Returns a new string with a specified number of copies of the original string.

// // // 13. **`replace(searchValue, replaceValue)`**: Replaces the first occurrence of a specified substring or regular expression with another substring.

// // // 14. **`search(regexp)`**: Searches for a match between a regular expression and the string, returning the index of the match.

// // // 15. **`slice(beginIndex, endIndex)`**: Extracts a section of the string and returns it as a new string.

// // // 16. **`split(separator, limit)`**: Splits the string into an array of substrings based on a specified separator.

// // // 17. **`startsWith(searchString, position)`**: Determines if the string starts with the specified substring.

// // // 18. **`substring(startIndex, endIndex)`**: Returns a substring from the start index up to, but not including, the end index.

// // // 19. **`toLowerCase()`**: Converts all characters in the string to lowercase.

// // // 20. **`toUpperCase()`**: Converts all characters in the string to uppercase.

// // // 21. **`trim()`**: Removes whitespace from both ends of the string.

// // // 22. **`trimEnd()`**: Removes whitespace from the end of the string.

// // // 23. **`trimStart()`**: Removes whitespace from the beginning of the string.

// // // 24. **`valueOf()`**: Returns the primitive value of the string object.

// // // 25. **`at(index)`**: Returns the character at the specified position, allowing for negative indices to count from the end.

// // // 26. **`toString()`**: Returns the string representation of the string object.



